    private void carregarTabela() {
    // Limpa a tabela
    javax.swing.table.DefaultTableModel model =
        (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    // Busca os dados
    controle.ElencoControle ec = new controle.ElencoControle();
    java.util.ArrayList<modelo.Elenco> lista = ec.consultarElenco();

    // Preenche linha por linha
    for (modelo.Elenco ele : lista) {
        model.addRow(new Object[]{
            ele.getID_Elenco(),
            ele.getNome(),
            ele.getFoto_elenco()
        });
    }
}
