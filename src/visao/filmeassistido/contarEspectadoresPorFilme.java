    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String idTexto = jTextField1.getText().trim();
    if (idTexto.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Digite um ID!");
        return;
    }
    int id = Integer.parseInt(idTexto);
    controle.Filme_AssistidosControle fac = new controle.Filme_AssistidosControle();
    int total = fac.contarEspectadoresPorFilme(id);
    
    controle.FilmeControle fc = new controle.FilmeControle();
    modelo.Filme filme = fc.consultarFilmeCodigo(id);
    
    
    
    jTextField2.setText(filme.getNome_filme() != null ? filme.getNome_filme() : "Filme não encontrado");
    jTextField3.setText(String.valueOf(total));
    }//GEN-LAST:event_jButton1ActionPerformed

