    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
    String idTexto = jTextField1.getText().trim();
    if (idTexto.isEmpty()) {
       javax.swing.JOptionPane.showMessageDialog(this, "Digite um ID!");
        return;
    }
    int id = Integer.parseInt(idTexto);
    controle.Filme_AssistidosControle fac = new controle.Filme_AssistidosControle();
    modelo.Filme_assistidos fa = fac.consultarID_Filme_assistidos(id);

    if (fa.getID_Assistidos() == 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Registro não encontrado!");
        return;
    }

    jFormattedTextField1.setText(fa.getData_assistido().toString());
    jTextField2.setText(fa.getID_Filme().getNome_filme());
    jTextField3.setText(fa.getID_Perfil().getNome_perfil());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String idTexto = jTextField1.getText().trim();
    if (idTexto.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Busque um registro primeiro!");
        return;
    }
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja realmente deletar?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        int id = Integer.parseInt(idTexto);
        controle.Filme_AssistidosControle fac = new controle.Filme_AssistidosControle();
        String resultado = fac.deletarID_Filme_assistidos(id);
        if (resultado.equals("removido")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Deletado com sucesso!");
            jButton3.doClick();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao deletar: " + resultado);
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

