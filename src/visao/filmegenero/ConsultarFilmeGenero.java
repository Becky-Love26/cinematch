    private void tblFilmeGeneroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFilmeGeneroMouseClicked
        if (evt.getClickCount() == 2) {
        int linha = tblFilmeGenero.getSelectedRow();
        if (linha == -1) return;

        AlterarFilmeGenero telaAlt = new AlterarFilmeGenero();
        telaAlt.carregarDados(
            tblFilmeGenero.getValueAt(linha, 0).toString(), // ID_Filme_Genero → spinner
            tblFilmeGenero.getValueAt(linha, 1).toString(), // ID_Genero
            tblFilmeGenero.getValueAt(linha, 3).toString()  // ID_Filme (coluna 3, não 2!)
        );
        telaAlt.setVisible(true);
    
    }

    }//GEN-LAST:event_tblFilmeGeneroMouseClicked

