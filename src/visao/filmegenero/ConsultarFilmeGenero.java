    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
         try {
        DefaultTableModel modelo = (DefaultTableModel) tblFilmeGenero.getModel();
        modelo.setNumRows(0);

        controle.Filme_GeneroControle fgc = new controle.Filme_GeneroControle();
        String texto = txtIDConsulta.getText().trim();

        if (texto.isEmpty()) {
            for (modelo.Filme_genero fg : fgc.consultarFilme_genero()) {
                modelo.addRow(new Object[]{
                    fg.getID_Filme_Genero(),
                    fg.getID_Genero().getID_Genero(),
                    fg.getID_Genero().getNome_genero(),   // nome do gênero
                    fg.getID_Filme().getID_Filme(),
                    fg.getID_Filme().getNome_filme()      // nome do filme
                });
            }
        } else {
            int id = Integer.parseInt(texto);
            modelo.Filme_genero fg = fgc.consultarID_Filme_Genero(id);

            if (fg != null && fg.getID_Filme_Genero() > 0) {
                modelo.addRow(new Object[]{
                    fg.getID_Filme_Genero(),
                    fg.getID_Genero().getID_Genero(),
                    fg.getID_Genero().getNome_genero(),
                    fg.getID_Filme().getID_Filme(),
                    fg.getID_Filme().getNome_filme()
                });
            } else {
                JOptionPane.showMessageDialog(this, "Nenhum registro encontrado para o ID " + id);
            }
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Digite apenas números no campo ID.",
            "Entrada inválida", JOptionPane.WARNING_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao consultar: " + e.getMessage());
    }
    }//GEN-LAST:event_btnConsultarActionPerformed

